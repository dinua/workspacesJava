package medweb.psconf.daos;

public class Consult {

	private int idConsult;
	private Fisa fisa;
	private String observatii;
	private String dataConsult;
	/**
	 * @return the idConsult
	 */
	public int getIdConsult() {
		return idConsult;
	}
	/**
	 * @param idConsult the idConsult to set
	 */
	public void setIdConsult(int idConsult) {
		this.idConsult = idConsult;
	}
	/**
	 * @return the fisa
	 */
	public Fisa getFisa() {
		return fisa;
	}
	/**
	 * @param fisa the fisa to set
	 */
	public void setFisa(Fisa fisa) {
		this.fisa = fisa;
	}
	/**
	 * @return the observatii
	 */
	public String getObservatii() {
		return observatii;
	}
	/**
	 * @param observatii the observatii to set
	 */
	public void setObservatii(String observatii) {
		this.observatii = observatii;
	}
	/**
	 * @return the dataConsult
	 */
	public String getDataConsult() {
		return dataConsult;
	}
	/**
	 * @param dataConsult the dataConsult to set
	 */
	public void setDataConsult(String dataConsult) {
		this.dataConsult = dataConsult;
	}
	
	
	
}
